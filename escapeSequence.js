//An escape sequence is a special character in a string that starts with a backslash \ and 
 // represents a character that is hard to type or invisible.
console.log("Hello\nWorld");   // Hello (new line) World
console.log("1\t2\t3");        // 1    2    3
console.log("He said: \"Hi\""); // He said: "Hi"
console.log("C:\\Users\\Noora"); // C:\Users\Noora
console.log("\u2764");          // ❤
