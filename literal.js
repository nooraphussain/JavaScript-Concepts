// A literal in JavaScript is a fixed value that you write directly in the code (not stored in a variable or computed at runtime).

// 👉 Basically, literals represent values directly written in the source code.

// Examples:

10              // number literal
"hello"         // string literal
true            // boolean literal
[1, 2, 3]       // array literal
// {a: 1, b: 2}    // object literal
null            // null literal
undefined       //Undefined Literal
/abc/i          //Regular Expression Literal
let name = "Noora";
let msg = `Hello,
This is ${name}!`;  //Template Literal